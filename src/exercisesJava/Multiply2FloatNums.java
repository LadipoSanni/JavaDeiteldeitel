package exercisesJava;

public class Multiply2FloatNums {
    public static void main(String[] args) {
        float fest = 3.0F;
        float secon = 24.7F;
        multiplyFloat(fest,secon);
    }

    private static void multiplyFloat(float fes, float secon) {
        float resut = fes*secon;
        System.out.println(resut);
    }
}
